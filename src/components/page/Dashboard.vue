<template>
    <div>
        <el-row :gutter="20">
            <el-col :span="8">
                <el-card shadow="hover" class="mgb20" style="height:262px;">
                    <div class="user-info">
                        <img src="../../assets/img/img.jpg" class="user-avator" alt="">
                        <div class="user-info-cont">
                            <div class="user-info-name">{{name}}</div>

                        </div>
                    </div>
                    <div class="user-info-list">用户账号<span>{{jobNo}}</span></div>
                    <div class="user-info-list">用户类别<span>{{role}}</span></div>
                    <div class="user-info-list">本机IP地址<span style="margin-left:50px;">{{IP}}</span></div>
                </el-card>
            </el-col>
         </el-row> 
         <el-row :gutter="20">
            <el-col :span="12">
                <el-card shadow="hover">
                    <schart ref="bar" class="schart" canvasId="bar" :data="data" type="bar" :options="options"></schart>
                </el-card>
            </el-col>
            <el-col :span="12">
                <el-card shadow="hover">
                    <schart ref="line" class="schart" canvasId="line" :data="data" type="line" :options="options2"></schart>
                </el-card>
            </el-col>
        </el-row>
    </div>
</template>

<script>
    import Schart from 'vue-schart';
    import bus from '../common/bus';
    export default {
        name: 'dashboard',
        data() {
            return {
                name: localStorage.getItem('username'),
                role1: window.localStorage['role'],
                jobNo: window.localStorage["jobNo"],
                IP: window.localStorage["IP"],
                data: [],
                options: {
                    title: '最近七天每天的用户访问量',
                    showValue: false,
                    fillColor: 'rgb(45, 140, 240)',
                    bottomPadding: 30,
                    topPadding: 30
                },
                options2: {
                    title: '最近七天用户访问趋势',
                    fillColor: '#FC6FA1',
                    axisColor: '#008ACD',
                    contentColor: '#EEEEEE',
                    bgColor: '#F5F8FD',
                    bottomPadding: 30,
                    topPadding: 30
                }
              
             
              
            }
        },
        components: {
            Schart
        },
        computed: {
            role() {
                if(this.role1 === 'ROLE_ADMIN'){
                    return '超级管理员';
                }else if(this.role1 === 'ROLE_TEACHER'){
                    return '教师';
                }else{
                    return '学生';
                }
               
            }
        },
        created(){
            this.handleListener();
            this.changeDate();
            this.getData();
        },
        activated(){
            this.handleListener();
        },
        deactivated(){
            window.removeEventListener('resize', this.renderChart);
            bus.$off('collapse', this.handleBus);
        },
        methods: {
            changeDate(){
                const now = new Date().getTime();
                this.data.forEach((item, index) => {
                    const date = new Date(now - (6 - index) * 86400000);
                    item.name = `${date.getFullYear()}/${date.getMonth()+1}/${date.getDate()}`
                })
            },
            handleListener(){
                bus.$on('collapse', this.handleBus);
                // 调用renderChart方法对图表进行重新渲染
                window.addEventListener('resize', this.renderChart)
            },
            handleBus(msg){
                setTimeout(() => {
                    this.renderChart()
                }, 300);
            },
            renderChart(){
                this.$refs.bar.renderChart();
                this.$refs.line.renderChart();
            },
             getData() {
              let _this = this
                this.$axios.get('/api/accessRecord/getRecentRecords', {
                }).then((res) => {
                    _this.data = res.data.data;
                })
            },

        }
    }

</script>


